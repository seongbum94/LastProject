package icare.memberManage.teacher.controller;

public class TeacherCommand  {
	
	private int tid; // 교번
	private String servstts; // 재직여부
	private String position; // 직무
	private String name; // 이름
	private String kinderName;
	private String author; // 권한
	
	public int getTid() {
		return tid;
	}
	public void setTid(int tid) {
		this.tid = tid;
	}
	public String getServstts() {
		return servstts;
	}
	public void setServstts(String servstts) {
		this.servstts = servstts;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getKinderName() {
		return kinderName;
	}
	public void setKinderName(String kinderName) {
		this.kinderName = kinderName;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	
	

	
}
