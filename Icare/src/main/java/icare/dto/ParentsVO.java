package icare.dto;
/**
 * 학부모 정보가 담긴 VO
 * @author LMY
 *
 */
public class ParentsVO {
	private String pid;	//학부모아이디
	private String account;	//계좌번호
	private String name;	//이름
	private String email;	//이메일
	private String addr1;	//주소
	private String addr2;	//상세주소
	private int zip;	//우편번호
	private String phone;	//전화번호
	private String author;	//권한
	private String pwd;	//비밀번호
	private String kid;	//유치원번호
	private String cno;	//반번호
	
	public ParentsVO() {
	}

	public ParentsVO(String pid, String account, String name, String email, String addr1, String addr2, int zip,
			String phone, String author, String pwd, String kid, String cno) {
		super();
		this.pid = pid;
		this.account = account;
		this.name = name;
		this.email = email;
		this.addr1 = addr1;
		this.addr2 = addr2;
		this.zip = zip;
		this.phone = phone;
		this.author = author;
		this.pwd = pwd;
		this.kid = kid;
		this.cno = cno;
	}

	public String getPid() {
		return pid;
	}

	public void setPid(String pid) {
		this.pid = pid;
	}

	public String getAccount() {
		return account;
	}

	public void setAccount(String account) {
		this.account = account;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getAddr1() {
		return addr1;
	}

	public void setAddr1(String addr1) {
		this.addr1 = addr1;
	}

	public String getAddr2() {
		return addr2;
	}

	public void setAddr2(String addr2) {
		this.addr2 = addr2;
	}

	public int getZip() {
		return zip;
	}

	public void setZip(int zip) {
		this.zip = zip;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}

	public String getPwd() {
		return pwd;
	}

	public void setPwd(String pwd) {
		this.pwd = pwd;
	}

	public String getKid() {
		return kid;
	}

	public void setKid(String kid) {
		this.kid = kid;
	}

	public String getCno() {
		return cno;
	}

	public void setCno(String cno) {
		this.cno = cno;
	}

	@Override
	public String toString() {
		return "ParentsVO [pid=" + pid + ", account=" + account + ", name=" + name + ", email=" + email + ", addr1="
				+ addr1 + ", addr2=" + addr2 + ", zip=" + zip + ", phone=" + phone + ", author=" + author + ", pwd="
				+ pwd + ", kid=" + kid + ", cno=" + cno + "]";
	}
	
}
