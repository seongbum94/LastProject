package icare.dto;
/**
 * @author 이준수
 * 유치원VO
 *
 */
public class KindergartenVO {
	private String kid; 						//유치원번호(pk)
	private String greeting;                //인사말
	private String intro;                   //소개
	private String directions;              //오시는길
	private String apad;                    //입소신청여부
	private String name;                    //이름
	private String addr1;                   //주소
	private String addr2;                   //상세주소
	private int zip;                     //우편번호
	private String tel;                     //전화번호
	private String fax;                     //팩스번호
	private String businesslicense;         //사업자등록번호
	private String accept;                  //승인여부
	
	public KindergartenVO() {
	}

	public KindergartenVO(String kid, String greeting, String intro, String directions, String apad, String name,
			String addr1, String addr2, int zip, String tel, String fax, String businesslicense, String accept) {
		super();
		this.kid = kid;
		this.greeting = greeting;
		this.intro = intro;
		this.directions = directions;
		this.apad = apad;
		this.name = name;
		this.addr1 = addr1;
		this.addr2 = addr2;
		this.zip = zip;
		this.tel = tel;
		this.fax = fax;
		this.businesslicense = businesslicense;
		this.accept = accept;
	}

	public String getKid() {
		return kid;
	}

	public void setKid(String kid) {
		this.kid = kid;
	}

	public String getGreeting() {
		return greeting;
	}

	public void setGreeting(String greeting) {
		this.greeting = greeting;
	}

	public String getIntro() {
		return intro;
	}

	public void setIntro(String intro) {
		this.intro = intro;
	}

	public String getDirections() {
		return directions;
	}

	public void setDirections(String directions) {
		this.directions = directions;
	}

	public String getApad() {
		return apad;
	}

	public void setApad(String apad) {
		this.apad = apad;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAddr1() {
		return addr1;
	}

	public void setAddr1(String addr1) {
		this.addr1 = addr1;
	}

	public String getAddr2() {
		return addr2;
	}

	public void setAddr2(String addr2) {
		this.addr2 = addr2;
	}

	public int getZip() {
		return zip;
	}

	public void setZip(int zip) {
		this.zip = zip;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public String getFax() {
		return fax;
	}

	public void setFax(String fax) {
		this.fax = fax;
	}

	public String getBusinesslicense() {
		return businesslicense;
	}

	public void setBusinesslicense(String businesslicense) {
		this.businesslicense = businesslicense;
	}

	public String getAccept() {
		return accept;
	}

	public void setAccept(String accept) {
		this.accept = accept;
	}

	@Override
	public String toString() {
		return "KindergartenVO [kid=" + kid + ", greeting=" + greeting + ", intro=" + intro + ", directions="
				+ directions + ", apad=" + apad + ", name=" + name + ", addr1=" + addr1 + ", addr2=" + addr2 + ", zip="
				+ zip + ", tel=" + tel + ", fax=" + fax + ", businesslicense=" + businesslicense + ", accept=" + accept
				+ "]";
	}
	
}
