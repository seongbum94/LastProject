package icare.dto;

import java.util.Date;

/**
 * 교사의 일정
 * @author LMY
 *
 */
public class TScheduleVO {
	private int tno;	//일정번호
	private Date startdate;	//시작날짜
	private Date enddate;		//종료날짜
	private String title;	//제목
	private String content;	//내용
	private String tid;	//교번
	
	public TScheduleVO() {
	}

	public TScheduleVO(int tno, Date startdate, Date enddate, String title, String content, String tid) {
		super();
		this.tno = tno;
		this.startdate = startdate;
		this.enddate = enddate;
		this.title = title;
		this.content = content;
		this.tid = tid;
	}

	public int getTno() {
		return tno;
	}

	public void setTno(int tno) {
		this.tno = tno;
	}

	public Date getStartdate() {
		return startdate;
	}

	public void setStartdate(Date startdate) {
		this.startdate = startdate;
	}

	public Date getEnddate() {
		return enddate;
	}

	public void setEnddate(Date enddate) {
		this.enddate = enddate;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getTid() {
		return tid;
	}

	public void setTid(String tid) {
		this.tid = tid;
	}

	@Override
	public String toString() {
		return "TScheduleVO [tno=" + tno + ", startdate=" + startdate + ", enddate=" + enddate + ", title=" + title
				+ ", content=" + content + ", tid=" + tid + "]";
	}
}
