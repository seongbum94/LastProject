package icare.dto;

import java.util.Date;

/**
 * 읽음여부
 * @author LMY
 *
 */
public class ReadVO {
	private String tid;	//교번
	private int eno;	//메일번호
	private Date receivedate;	//수신일
	
	public ReadVO() {
	}

	public ReadVO(String tid, int eno, Date receivedate) {
		super();
		this.tid = tid;
		this.eno = eno;
		this.receivedate = receivedate;
	}

	public String getTid() {
		return tid;
	}

	public void setTid(String tid) {
		this.tid = tid;
	}

	public int getEno() {
		return eno;
	}

	public void setEno(int eno) {
		this.eno = eno;
	}

	public Date getReceivedate() {
		return receivedate;
	}

	public void setReceivedate(Date receivedate) {
		this.receivedate = receivedate;
	}

	@Override
	public String toString() {
		return "ReadVO [tid=" + tid + ", eno=" + eno + ", receivedate=" + receivedate + "]";
	}
	
}
