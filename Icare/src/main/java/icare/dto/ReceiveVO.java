package icare.dto;

import java.util.Date;

/**
 * 메일수신
 * @author LMY
 *
 */
public class ReceiveVO {
	private int reno;	//메일수신번호
	private String receiveref;	//수신참조
	private int eno;	//메일번호
	private String tid;	//교번
	
	public ReceiveVO() {
	}

	public ReceiveVO(int reno, String receiveref, int eno, String tid) {
		super();
		this.reno = reno;
		this.receiveref = receiveref;
		this.eno = eno;
		this.tid = tid;
	}

	public int getReno() {
		return reno;
	}

	public void setReno(int reno) {
		this.reno = reno;
	}

	public String getReceiveref() {
		return receiveref;
	}

	public void setReceiveref(String receiveref) {
		this.receiveref = receiveref;
	}

	public int getEno() {
		return eno;
	}

	public void setEno(int eno) {
		this.eno = eno;
	}

	public String getTid() {
		return tid;
	}

	public void setTid(String tid) {
		this.tid = tid;
	}

	@Override
	public String toString() {
		return "ReceiveVO [reno=" + reno + ", receiveref=" + receiveref + ", eno=" + eno + ", tid=" + tid + "]";
	}
}
